class Solution {
public:
    int unequalTriplets(vector<int>& nums) {
        vector<int> CountOfSameNumber(1001,0);
        int MaxOfNum = 0;
        for(const auto i:nums)
        {
        	CountOfSameNumber[i]++;
        	if(i>MaxOfNum)
        		MaxOfNum = i;
        }
        int res = 0;
        for(int i=1; i<=MaxOfNum; i++){
        	if(CountOfSameNumber[i]==0)
        		continue;
        	for(int j=i+1; j<=MaxOfNum; j++){
        		if(CountOfSameNumber[j]==0)
        			continue;
        		for(int k=j+1;k<=MaxOfNum; k++){
        			if(CountOfSameNumber[k]==0)
        				continue;
        			res = res + CountOfSameNumber[i]*CountOfSameNumber[j]*CountOfSameNumber[k];
        		}
        	}
        }
        return(res);
    }
    
private:
	vector<queue<TreeNode*>> BFS(TreeNode* root){
        queue<TreeNode*> q1;
        // cout<<"Starting BFS\n"<<endl;
        // fflush(stdout);
        vector<queue<TreeNode*>> Res;
        q1.push(root);
        do{
            Res.push_back(q1);
            queue<TreeNode*> q2;
            while(!q1.empty()){
                TreeNode* s = q1.front();
                q1.pop();
                if(s->left != nullptr)
                    q2.push(s->left);
                if(s->right != nullptr)
                    q2.push(s->right);
            }
            q1 = q2;  
            // PrintQueueInIntBinaryTree(q1);
        }while(!q1.empty());
        return(Res);
    }
	
};
